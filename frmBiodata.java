    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        
        DefaultTableModel tbl = (DefaultTableModel)tblMhs.getModel();
        
        try {
            String namaFile = FileSystemView.getFileSystemView().getDefaultDirectory().getPath()+ "\\dataMahasiswa.txt";
            FileReader fr = new FileReader(namaFile);
            Scanner br = new Scanner(fr);
            
            while (br.hasNextLine()) {
                String line = br.nextLine();
                String[] isi = line.split(Pattern.quote("|"));
                tbl.addRow(isi);
               
                
            }
        }
        
        catch (Exception e){
            
            JOptionPane.showMessageDialog(this, e);
            
   
            
        }
        
    }//GEN-LAST:event_formComponentShown

