    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        try {
            
            String namaFile = FileSystemView.getFileSystemView().getDefaultDirectory().getPath()+ "\\dataMahasiswa.txt";
            FileWriter fw = new FileWriter(namaFile);
            BufferedWriter br = new BufferedWriter(fw);
            
            for (int i=0;i<tblMhs.getRowCount();i++){
                
                br.write(tblMhs.getValueAt(i, 0) + "|");
                br.write(tblMhs.getValueAt(i, 1) + "|");
                br.write(tblMhs.getValueAt(i, 2) + "|");
                br.write(tblMhs.getValueAt(i, 3) + "|");      
                br.newLine();
                
            }
        
            br.close();
            
            JOptionPane.showMessageDialog(this, "Data telah disimpan");
            
        } catch (Exception e) {
        
            
            JOptionPane.showMessageDialog(this, "File tidak ditemukan");
            
            
        }
        
        
        
    }//GEN-LAST:event_btnSimpanActionPerformed

